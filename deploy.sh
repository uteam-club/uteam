#!/bin/bash

# Скрипт для выполнения всех необходимых шагов при деплое в продакшен

echo "🚀 Начинаем процесс деплоя в продакшен..."

# 1. Генерируем Prisma клиент
echo "📊 Генерируем Prisma клиент..."
npx prisma generate
if [ $? -ne 0 ]; then
    echo "❌ Ошибка при генерации Prisma клиента"
    exit 1
fi
echo "✅ Prisma клиент успешно сгенерирован"

# 2. Применяем миграции
echo "🔄 Применяем миграции базы данных..."
npx prisma migrate deploy
if [ $? -ne 0 ]; then
    echo "❌ Ошибка при применении миграций"
    exit 1
fi
echo "✅ Миграции успешно применены"

# 3. Запускаем скрипт для восстановления связей в базе данных
echo "🔗 Запускаем скрипт для восстановления связей в базе данных..."
node production-deploy.js
if [ $? -ne 0 ]; then
    echo "❌ Ошибка при восстановлении связей в базе данных"
    exit 1
fi
echo "✅ Связи в базе данных успешно восстановлены"

# 4. Очистка кэша Next.js
echo "🧹 Очистка кэша Next.js..."
rm -rf .next/cache
echo "✅ Кэш Next.js успешно очищен"

# 5. Построение приложения
echo "🏗️ Собираем приложение..."
npm run build
if [ $? -ne 0 ]; then
    echo "❌ Ошибка при сборке приложения"
    exit 1
fi
echo "✅ Приложение успешно собрано"

# 6. Запуск приложения
echo "🚀 Запускаем приложение..."
npm run start

echo "✨ Деплой успешно завершен!" 